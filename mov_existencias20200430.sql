-- -----------------------------------------------------------
-- SIGUIENTE TABLA
-- AQUI SE OBTIENE EL NUMERO TOTAL DE UNIDADES POR CLASE-FAMILIA
CREATE TABLE UNIDADES_IMPORTE_TOTALES_20200430 AS -- por CLASE-FAMILIA
SELECT
des_Area,
idu_DepartamentoCodigo,
des_Departamento,
idu_ClaseCodigo,
des_Clase,
idu_FamiliaCodigo,
des_Familia,
SUM(UNIDADES_TOTALES) AS UNIDADES_TOTALES_CLASE_FAMILIA, -- SUMA LAS UNIDADES DE CADA ARTICULO QUE PERTENECE A LA MISMA CLASE-FAMILIA
SUM(IMPORTE_TOTAL) AS IMPORTE_TOTALES_CLASE_FAMILIA -- SUMA LOS IMPORTES DE CADA ARTICULO QUE PERTENECE A LA MISMA CLASE-FAMILIA
FROM CODIGO_ARTICULO_E_IMPORTE_TOTAL_20200430
GROUP BY des_Area, idu_DepartamentoCodigo,des_Departamento,idu_ClaseCodigo,des_Clase,idu_FamiliaCodigo,des_Familia
ORDER BY UNIDADES_TOTALES_CLASE_FAMILIA DESC;

drop table UNIDADES_IMPORTE_TOTALES_20200430;
select  * from UNIDADES_IMPORTE_TOTALES_20200430;


